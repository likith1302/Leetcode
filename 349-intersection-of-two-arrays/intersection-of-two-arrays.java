class Solution {
    public int[] intersection(int[] nums1, int[] nums2) {
        ArrayList<Integer> r=new ArrayList<>();
         Set<Integer> set1 = new HashSet<>();
         Set<Integer> set2 = new HashSet<>();
        for (int num : nums2) {
            set2.add(num);  
        }

        for (int num : nums1) {
            set1.add(num);  
        }
        for(int i:set1){
            if(set2.contains(i)){
              r.add(i);
            }
        }
        int[] result = new int[r.size()];
        for (int i = 0; i < r.size(); i++) {
            result[i] = r.get(i);
        }

        return result;
    }
}